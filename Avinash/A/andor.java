import java.util.Scanner;

class Solution {
    public static void main(String args[]){
        Scanner pp = new Scanner(System.in);
        int t = pp.nextInt();
        for(int T = 0;T<t;T++){
            long n = pp.nextLong();
            System.out.println(0 +" " + n);
            
        }
        pp.close();
    }

}