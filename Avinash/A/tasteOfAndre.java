import java.util.Scanner;

public class Solution {
    public static void main(String args[]){
        Scanner pp = new Scanner(System.in);
        int t = pp.nextInt();
        for(int T = 0;T<t;T++){
            int n = pp.nextInt();
            for(int i = 0;i<n;i++){
                System.out.print(1 + " ");
            }
        }
        pp.close();
    }
}